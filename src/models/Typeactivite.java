package models;

import java.util.Objects;

/**
 *
 * @author karbo
 */
public class Typeactivite {
    
    private int id ;
    private String type;

    public Typeactivite(int id, String type) {
        this.id = id;
        this.type = type;
    }

    public Typeactivite(String type) {
        this.type = type;
    }

    public Typeactivite() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 53 * hash + this.id;
        hash = 53 * hash + Objects.hashCode(this.type);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Typeactivite other = (Typeactivite) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.type, other.type)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Typeactivite{" + "id=" + id + ", type=" + type + '}';
    }
    
    
    
}
